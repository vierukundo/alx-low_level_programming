#include "lib.h"
/**
 * add - adds two numbers
 * @a: first operand
 * @b: second operand
 * Return: result of the calcutions
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two numbers
 * @a: first operand
 * @b: second operand
 * Return: result of the calcutions
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiply two numbers
 * @a: first operand
 * @b: second operand
 * Return: result of the calcutions
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two numbers
 * @a: first operand
 * @b: second operand
 * Return: result of the calcutions
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - determine the modulo two numbers
 * @a: first operand
 * @b: second operand
 * Return: result of the calcutions
 */
int mod(int a, int b)
{
	return (a % b);
}
